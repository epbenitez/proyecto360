package com.allinone.domain;

/**
 *
 * @author patriciabenitez
 */
public class Indicadores {
    
    private Long total;
    private Long abiertos;
    private Long vencidos;
    private Long enTiempo;
    private Long visibles;
    private Long cuota;

    public Long getTotal() {
        return total;
    }

    public void setTotal(Long total) {
        this.total = total;
    }

    public Long getAbiertos() {
        return abiertos;
    }

    public void setAbiertos(Long abiertos) {
        this.abiertos = abiertos;
    }

    public Long getVencidos() {
        return vencidos;
    }

    public void setVencidos(Long vencidos) {
        this.vencidos = vencidos;
    }

    public Long getEnTiempo() {
        return enTiempo;
    }

    public void setEnTiempo(Long enTiempo) {
        this.enTiempo = enTiempo;
    }

    public Long getVisibles() {
        return visibles;
    }

    public void setVisibles(Long visibles) {
        this.visibles = visibles;
    }

    public Long getCuota() {
        return cuota;
    }

    public void setCuota(Long cuota) {
        this.cuota = cuota;
    }
    
    
}
